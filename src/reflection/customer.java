package reflection;

public class customer {
    private String name;
    private String age;

    public customer() {
    }

 public String getName() {
        return name;
    }

    private void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
